


//add hovered class in selected list item
let list = document.querySelectorAll('.navigateur button');
function activeLink(){ 
list.forEach((item) =>
item.classList.remove('hovered'));
this.classList.add('hovered');
}
list.forEach((item) =>
item.addEventListener('mouseover', activeLink));



